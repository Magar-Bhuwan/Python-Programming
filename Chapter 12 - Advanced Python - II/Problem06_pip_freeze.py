# Run pip freeze for the system interpreter. Take the contents and create a similar virtual environment.

## process:
# 1. Run "pip freeze > requirements.txt" in the terminal to save the list of installed packages to a file.
# 2. Create a new virtual environment using python -m venv myenv.
# 3. Activate the virtual environment:
#    - On Windows: myenv\Scripts\activate
#    - On macOS/Linux: source myenv/bin/activate
# 4. Install the packages from the requirements.txt file using pip install -r requirements.txt




