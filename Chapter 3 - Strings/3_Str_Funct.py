
name = "ramlal chaudhary"
print(len(name))                                #len()function
print(name.endswith("lal"))                     #string.endswith("")
print(name.startswith("ram"))                   #string.startswith("")
print(name.capitalize())                        #string.capitalize() --> Only for starting character
print(name.upper())                             #string.upper() --> all Capital letter
print(name.count("a"))                          #string.count("a") --> count character
print(name.find("chaudhary"))                   #name.find("chaudhary") --> starting index no.
print(name.replace("ramlal","hari"))            #name.replace("","") replace the characters
