# 6 + 5 = 11
#where 6 and 5 are operands and + and = are operators and 11 is results

# Arithmetic Operator
a = 4
b = 5
c = a+b
print(c)

# Assignment Operator
d = 5-4  #assign 5-4 in d
print(d)

e = 6
e += 6  #increment operator(Operators should(+=,-=,*=,/=))
print(e)

# Comparision Operator (==, >=, <=, !=, >, <, etc)
f = 6>7
print(f)

# Logical Operator (or, and, not)
g = True or False
print(g)

