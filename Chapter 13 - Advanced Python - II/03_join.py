# Join method is used to concatenate a list of strings into a single string, with a specified separator between each element.
# The syntax for the join method is: 'separator'.join(iterable)

# Example 01:
a = ["Hello", "World", "Python", "Programming"]

result = " ".join(a)
print(result)


# Example 02:
b = ["2024", "06", "30"]
result2 = "-".join(b)
print(result2)

