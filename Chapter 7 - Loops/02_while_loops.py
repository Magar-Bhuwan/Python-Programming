# a while loop is a control flow statement that repeatedly executes a block of code
# as long as a specified condition is true.

'''
1. The condition is evaluated before each loop iteration
2. If the condition is True, the code block runs.
3. If the condition becomes False, the loop stops.

'''
#Example: print 1- 5 numbers.

i = 1

while(i <= 5):
    print(i)
    i += 1          # i = i + 1

