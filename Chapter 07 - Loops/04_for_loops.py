'''
=> A for loop is used to iterate over a sequence (such as a list, tuple, string, or ranage)
 and execute a block of code once for each item in that sequence.
  
 => It is especially useful when you know in advance how many times you want to 
 repeat an action.

 => To repeat an action a specific number of times
 => To process every item in a collection (list, string, dictionary, etc)

 => There should be nested for loop with else

'''
for i in range(0, 50, 4):     # range index(0 = starting rage) (50 = stoping range) and (4 = step_size/gaps)
    print(i)
